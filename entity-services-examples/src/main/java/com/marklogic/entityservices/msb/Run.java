package com.marklogic.entityservices.msb;

public class Run {
    
    private String place;
    private int bib;
    private String Name;
    private String firstName;
    private String lastName;
    private String teamName;
    private String distance;
    private String category;
    private int age;
    private String gender;
    private String time;
    private String difference;
    private float percentageBack;
    private float percentageWinning;
    private float percentageAverage;
    
    public Run() {
    }
    
    public String getPlace() {
        return place;
    }
    
    public void setPlace(String place) {
        this.place = place;
    }
    
    public int getBib() {
        return bib;
    }
    
    public void setBib(int bib) {
        this.bib = bib;
    }
    
    public String getName() {
        return Name;
    }
    
    public void setName(String name) {
        Name = name;
    }
    
    public String getFirstName() {
        return firstName;
    }
    
    public void setFirstName(String firstName) {
        this.firstName = firstName;
    }
    
    public String getLastName() {
        return lastName;
    }
    
    public void setLastName(String lastName) {
        this.lastName = lastName;
    }
    
    public String getTeamName() {
        return teamName;
    }
    
    public void setTeamName(String teamName) {
        this.teamName = teamName;
    }
    
    public String getDistance() {
        return distance;
    }
    
    public void setDistance(String distance) {
        this.distance = distance;
    }
    
    public String getCategory() {
        return category;
    }
    
    public void setCategory(String category) {
        this.category = category;
    }
    
    public int getAge() {
        return age;
    }
    
    public void setAge(int age) {
        this.age = age;
    }
    
    public String getGender() {
        return gender;
    }
    
    public void setGender(String gender) {
        this.gender = gender;
    }
    
    public String getTime() {
        return time;
    }
    
    public void setTime(String time) {
        this.time = time;
    }
    
    public String getDifference() {
        return difference;
    }
    
    public void setDifference(String difference) {
        this.difference = difference;
    }
    
    public float getPercentageBack() {
        return percentageBack;
    }
    
    public void setPercentageBack(float percentageBack) {
        this.percentageBack = percentageBack;
    }
    
    public float getPercentageWinning() {
        return percentageWinning;
    }
    
    public void setPercentageWinning(float percentageWinning) {
        this.percentageWinning = percentageWinning;
    }
    
    public float getPercentageAverage() {
        return percentageAverage;
    }
    
    public void setPercentageAverage(float percentageAverage) {
        this.percentageAverage = percentageAverage;
    }
    
}
